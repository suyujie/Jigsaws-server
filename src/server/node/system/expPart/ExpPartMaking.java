package server.node.system.expPart;

public class ExpPartMaking {

	private Integer id;
	private String name;
	private int useExpStr;

	public ExpPartMaking() {
		super();
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getUseExpStr() {
		return useExpStr;
	}

	public void setUseExpStr(int useExpStr) {
		this.useExpStr = useExpStr;
	}

}